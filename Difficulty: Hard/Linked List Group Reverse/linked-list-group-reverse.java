class Solution {
    public static Node reverseKGroup(Node head, int k) {
        int list[]=new int[100000],ind=0;
        Node temp=head;
        while(temp!=null){
            list[ind++]=temp.data;
            temp=temp.next;
        }
        int y=ind;
        Node newnode=new Node(-1);
        temp=newnode;
        int m=k,n1=-1;
        if(ind<k) k=ind;
        k--;
        while(y-->0){
            Node t=new Node(list[k--]);
            temp.next=t;
            temp=t;
            if(k==n1){
                k+=m+m;
                if(k>=ind) k=ind-1;
                n1=k-m;
            }
        }
        return newnode.next;
    }
}
